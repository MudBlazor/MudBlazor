namespace MudBlazor;

/// <summary>
/// Indicates the action which displays a <see cref="MudMenu"/>.
/// </summary>
public enum MouseEvent
{
    /// <summary>
    /// The left mouse button was clicked.
    /// </summary>
    LeftClick,

    /// <summary>
    /// The right mouse button was clicked.
    /// </summary>
    RightClick,

    /// <summary>
    /// The mouse is hovering over the activator.
    /// </summary>
    MouseOver
}
